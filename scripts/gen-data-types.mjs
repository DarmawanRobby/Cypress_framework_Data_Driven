// Generates a union type of available data files so `data('...')` autocompletes
// and rejects typos. Run after adding/removing a data/*.json file:
//   npm run data:types
import { readdirSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const files = readdirSync(join(ROOT, 'data'))
  .filter((f) => f.endsWith('.json'))
  .map((f) => f.replace(/\.json$/, ''))
  .sort()

const union = files.length ? files.map((f) => `'${f}'`).join(' | ') : 'string'
const out = `// AUTO-GENERATED by \`npm run data:types\`. Do not edit by hand.\ntype DataFile = ${union}\n`
writeFileSync(join(ROOT, 'cypress', 'support', 'data-files.d.ts'), out)
console.log(`✓ data-files.d.ts → ${files.join(', ') || '(none)'}`)
